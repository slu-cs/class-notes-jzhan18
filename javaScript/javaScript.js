// Expression syntax in JavaScript

// Booleans
true
false

// Logic
!false
true && false
true || false

// Numbers (all one type)
3.14

// Equality
3 === 3.0
3 !== 3.14

// ordering
3 > 2
3 >= 2
2 < 3
2 <= 3

// Strings
'hello'

// concatenation
'a' + 'b' === 'ab'

// arithmetic
3 + 2 === 5
3 - 2 === 1
3 * 2 === 6
3 / 2 === 1.5
3 % 2 === 1
3 ** 2 === 9

// type coercion
'3' + 2 === '32'
'3' + true === '3true'
'3' - 2 === 1 // cannot subtract string
3 + true === 4

// loose Equality
'3' == 3 // true
true == 1
false == 0
false == ''
'' == 0

// template Strings
`3+2 is ${3+2}`
